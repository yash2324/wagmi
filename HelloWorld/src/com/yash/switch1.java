package com.yash;

import java.util.Scanner;

public class switch1 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int n = in.nextInt();
        switch (n)
        {
            case 1:
                System.out.println("hello");
                break;
            case 2:
                System.out.println("World");
                break;
        }

    }
}
